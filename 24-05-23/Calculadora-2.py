num1 = float(input("Informe o primeiro numero: "))

num2 = float(input("Informe o segundo numero: "))

operacao = input("Escolha a operacao: ")

if operacao == "adicao":
    print( num1 + num2 )
elif operacao == "subtracao":
    print( num1 - num2 )
elif operacao == "divisao":
    print( num1 / num2 )
elif operacao == "multiplicacao":
    print( num1 * num2 )
    


